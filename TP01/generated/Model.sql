CREATE TABLE SEDE (
	id INT NOT NULL,
	ciudad VARCHAR(20) NOT NULL,
	descripcion VARCHAR(20) NOT NULL,
	PRIMARY KEY (id)
);
CREATE TABLE PAIS (
	iso INT NOT NULL,
	nombre VARCHAR(20) NOT NULL,
	PBI DOUBLE NOT NULL,
	PRIMARY KEY (iso)
);
CREATE TABLE SECCION (
	id INT NOT NULL,
	PRIMARY KEY (id)
);
CREATE TABLE REGION (
	id INT NOT NULL,
	nombre VARCHAR(20) NOT NULL,
	PRIMARY KEY (id)
);
CREATE TABLE RED_SOCIAL (
	id INT NOT NULL,
	url VARCHAR(20) NOT NULL,
	nombre_red VARCHAR(20) NOT NULL,
	PRIMARY KEY (id)
);
CREATE TABLE pertenece_a (
	id INT NOT NULL,
	iso INT NOT NULL,
	PRIMARY KEY (id, iso),
	FOREIGN KEY (id) REFERENCES SEDE (id) ON DELETE CASCADE,
	FOREIGN KEY (iso) REFERENCES PAIS (iso) ON DELETE CASCADE
);
CREATE TABLE utiliza (
	id INT NOT NULL,
	id2 INT NOT NULL,	-- renamed from: id
	PRIMARY KEY (id, id2),
	FOREIGN KEY (id) REFERENCES SEDE (id) ON DELETE CASCADE,
	FOREIGN KEY (id2) REFERENCES RED_SOCIAL (id) ON DELETE CASCADE
);
CREATE TABLE posee (
	id INT NOT NULL,
	id2 INT NOT NULL,	-- renamed from: id
	PRIMARY KEY (id, id2),
	FOREIGN KEY (id) REFERENCES SEDE (id) ON DELETE CASCADE,
	FOREIGN KEY (id2) REFERENCES SECCION (id) ON DELETE CASCADE
);
CREATE TABLE se_ubica_en (
	iso INT NOT NULL,
	id INT NOT NULL,
	PRIMARY KEY (iso, id),
	FOREIGN KEY (iso) REFERENCES PAIS (iso) ON DELETE CASCADE,
	FOREIGN KEY (id) REFERENCES REGION (id) ON DELETE CASCADE
);
